    private void jTableProductosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableProductosMouseClicked
        try {
            LimpiaForm();
                    
            DefaultTableModel objTab = (DefaultTableModel)this.jTableProductos.getModel();
            int i=jTableProductos.getSelectedRow();
            if(i==-1){
                JOptionPane.showInternalMessageDialog(this, "Debe seleccionar un producto para editarlo");
            }else{
                int id = Integer.parseInt(String.valueOf(jTableProductos.getValueAt(i,0)));
                Precios objPre = new Precios(id, 0,0, 0,0);
                objPre.DamePrecios_Margenes();
                jTxtId.setText(""+id);
                jTxtUCosto.setText(""+objPre.getUltimo_costo());
                jTxtCostoPP.setText(""+objPre.getCosto_pp());
                jTxtPVenta.setText(""+objPre.getVenta());
                jTxtMCSPVUC.setText(String.format("%.2f",objPre.getMcsv_uc()));
                jTxtMCSPVCPP.setText(String.format("%.2f",objPre.getMcsv_cpp()));
                jTxtMCSPCUC.setText(String.format("%.2f",objPre.getMcsc_uc()));
                jTxtMCSPCCPP.setText(String.format("%.2f",objPre.getMcsc_cpp()));
                jTxtPrecio.requestFocus();
            }
        }catch(Exception e){
            System.err.println("error jTableProductosMouseClicked:"+e);
        }
    }//GEN-LAST:event_jTableProductosMouseClicked

