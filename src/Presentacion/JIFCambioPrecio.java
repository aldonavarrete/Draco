    private void jTxtPrecioFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTxtPrecioFocusLost
        try {
            if(!jTxtPrecio.getText().isEmpty()){
                int precio = Integer.parseInt(jTxtPrecio.getText());
                double costo_uc=Double.parseDouble(jTxtUCosto.getText());
                double costo_cpp=Double.parseDouble(jTxtCostoPP.getText());
                if(precio>0){
                    double uti_uc=(precio- costo_uc);
                    double uti_pp=(precio-costo_cpp);
                    jTxtVuc.setText(String.format("%.2f",((uti_uc/precio)*100)));
                    jTxtVcpp.setText(String.format("%.2f",((uti_pp/precio)*100)));
                    jTxtCuc.setText("0");
                    if(costo_uc>0){jTxtCuc.setText(String.format("%.2f",((uti_uc/costo_uc)*100)));}
                    jTxtCcpp.setText("0");
                    if(costo_cpp>0){jTxtCcpp.setText(String.format("%.2f",((uti_uc/costo_cpp)*100)));}
                    //System.out.println("Utilidad uc: "+uti_uc+"  Utilidad cpp: "+uti_pp);
                }
            }
        } catch (Exception e) {
            System.err.println("error jTxtPrecioFocusLost: "+e);
        }
        
        
    }//GEN-LAST:event_jTxtPrecioFocusLost

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            if(!jTxtPrecio.getText().isEmpty()){
                int precio = Integer.parseInt(jTxtPrecio.getText());
                if(precio>0 && precio>0){
                    Precios objPre = new Precios(Integer.parseInt(jTxtId.getText()), Integer.parseInt(jTxtPrecio.getText()), 0, 0, 0);
                    objPre.ActualizaPrecio();
                    JOptionPane.showMessageDialog(null, "Precio actualizado !!!");
                    LimpiaForm();
                    jTableProductos.requestFocus();
                }else{
                    JOptionPane.showMessageDialog(null, "Revise precio de venta");
                }
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_jButton1ActionPerformed

