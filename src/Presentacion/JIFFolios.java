    private void jListArchivosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jListArchivosMouseClicked
        try {
            int i=jListArchivos.getSelectedIndex();
            if(i==-1){
                JOptionPane.showMessageDialog(null, "Debe seleccionar un archivo!!!");
            }else{
                String nombreArchivo=jListArchivos.getSelectedValue();
                
                System.out.println("Archivo: "+nombreArchivo);
                System.out.println("RUT: "+rutEmpresa);
                
                LeeCAF objCAF = new LeeCAF(rutEmpresa, nombreArchivo);
                objCAF.LeeXML();
                
                jTextFecha.setText(objCAF.getFecha());
                jTextDesde.setText(""+objCAF.getrInicial());
                jTextHasta.setText(""+objCAF.getrFinal());
                jTextDocumento.setText(""+objCAF.getTipo());
                jTextRazonSocial.setText(objCAF.getRazonSocial());
                jTextRut.setText(objCAF.getRut());
            }
        } catch (Exception e) {
            System.err.println("Error jListArchivosMouseClicked: "+e);
        }
    }//GEN-LAST:event_jListArchivosMouseClicked

