    private void LeeDirectorio(){
        try {
            File dir = new File(System.getProperty("user.home")+File.separator+".local"+File.separator+"caf");
            String[] ficheros = dir.list();
            
            DefaultListModel modelo = new DefaultListModel();
            
            if (ficheros == null){
                System.out.println("No hay archivos en el directorio CAF");
            }else {
                jListArchivos.setModel(modelo);
                for (int i=0;i<ficheros.length;i++){
                    modelo.addElement(ficheros[i]);
                    //System.out.println(nombres_archivos[i]);
                }
            }
        } catch (Exception e) {
            System.err.println("Error LeeDirectorio: "+e);
        }   
    }
    
