    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            int i=jListArchivos.getSelectedIndex();
            if(i==-1){
                JOptionPane.showMessageDialog(null, "No hay un archivo seleccionado !!!");
                return;
            }

            if(jTextFecha.getText().isEmpty() || jTextRut.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Seleccione un archivo !!!");
            }else{
                //VALIDO QUE NO ESTE INGRESADO 
                Folios objFol = new Folios(Integer.parseInt(jTextDocumento.getText()),Integer.parseInt(jTextDesde.getText()),jTextFecha.getText(),Integer.parseInt(jTextDesde.getText()),Integer.parseInt(jTextHasta.getText()),jListArchivos.getSelectedValue());
                boolean ocupado = objFol.ExisteFolio();
                if(ocupado){
                    JOptionPane.showMessageDialog(null, "Folios ya ingresados");

                }else{
                    //Ingreso Folios
                    objFol.AgregarFolios();
                    JOptionPane.showMessageDialog(null, "Folios agregados !!!");
                    jTextDesde.setText(null);
                    jTextDocumento.setText(null);
                    jTextFecha.setText(null);
                    jTextHasta.setText(null);
                    jTextRazonSocial.setText(null);
                    jTextRut.setText(null);
                }
            }
            
        } catch (Exception e) {
        }
    }//GEN-LAST:event_jButton1ActionPerformed


