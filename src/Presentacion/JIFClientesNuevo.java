    private void jBtnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnGrabarActionPerformed
        try {
            if(jTxtRut.getText().isEmpty() || jTxtNombre.getText().isEmpty() || jTxtDireccion.getText().isEmpty() || jTxtCiudad.getText().isEmpty() || jTxtComuna.getText().isEmpty() || jTxtGiro.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "Faltan datos requeridos", "Clientes", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            int idCli=0;
            if(!Clientes.ClienteNuevo){
                idCli=Integer.parseInt( jTxtId.getText());
            }
            int cliAct=1;
            if(!jChkActivo.isSelected()){
                cliAct=0;
            }
            Clientes objCli = new Clientes(idCli, jTxtRut.getText(), jTxtNombre.getText(), jTxtDireccion.getText(), jTxtCiudad.getText(), jTxtComuna.getText(), jTxtGiro.getText(), cliAct);
            objCli.IprimeCliente();
            if(Clientes.ClienteNuevo){
                objCli.Grabar();
                jTxtRut.setText("");
                jTxtNombre.setText("");
                jTxtDireccion.setText("");
                jTxtCiudad.setText("");
                jTxtComuna.setText("");
                jTxtGiro.setText("");
                jTxtRut.requestFocus(true);
            }else{
                objCli.Actualizar();
                this.dispose();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_jBtnGrabarActionPerformed

