    private void jTxtRutFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTxtRutFocusLost
        try {
            if (Main.validarRut(jTxtRut.getText()) == false && !(jTxtRut.getText().isEmpty())) {
                JOptionPane.showMessageDialog(this, "Rut incorrecto, revise", "Error", JOptionPane.INFORMATION_MESSAGE);
                jBtnGrabar.setEnabled(false);
                return;
            }else{
                jBtnGrabar.setEnabled(true);
            }
            //VALIDO SI OTRO USUARIO TIENE MISMO RUT
            if(Clientes.ClienteNuevo==true){
                Clientes objCli = new Clientes(0,jTxtRut.getText(),"","","","","",0);
                objCli.ExisteRutCliente();
                if(Clientes.ExisteRUTCliente){
                    int msg=JOptionPane.showConfirmDialog(null, "Existe otro cliente con mismo rut, desae continuar", "Nuevo cliente", 0);
                    if(msg!=JOptionPane.YES_OPTION){
                        jTxtRut.setText(null);
                        jTxtRut.requestFocus();
                        return;
                    }
                }
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_jTxtRutFocusLost

